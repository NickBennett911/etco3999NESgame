#define TILE 0xd8
#define ATTR 0x01
#define ATTR2 0x61

#define NUM_BULLETS 5

typedef struct {
  int dir;
  int speed;
  int xpos;
  int ypos;
  bool in_use;
  int lifetime;
}Bullet;

Bullet bullets[NUM_BULLETS];

void init_bullet_list() {
  int i;
  for ( i = 0; i < NUM_BULLETS; i++) {
    bullets[i].in_use = false;
    bullets[i].lifetime = 30;
    bullets[i].speed = 2;
  }
}

void spawn_bullet(int px, int py, int dir) {
  	int i;
        vram_adr(NTADR_A(1, 2));
	for (i = 0; i < NUM_BULLETS; i++) {
        	if (!bullets[i].in_use) {
                    
                    vram_put(0x44);
                	bullets[i].in_use = true;
                  	bullets[i].xpos = px;
                  	bullets[i].ypos = py;
                  	bullets[i].dir = dir;
                  	return;
                }
        }
  	return;
}

bool active_bullet() {
  	int i;
	for (i = 0; i < NUM_BULLETS; i++) {
        	if (bullets[i].in_use) {
                	return true;
                }
        }
  	return false;
}



